package face.search.bean;

public class PhotoEvalInfo {
	private String name;
	private String path;
	private String environment;
	private String lighting;
	private String hair_color;
	private String gender;
	private String blurry;
	private String mustache;
	private String race;
	private String age;
	private String eye_wear;
	private String smiling;
	private String error;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getEnvironment() {
		return environment;
	}
	public void setEnvironment(String environment) {
		this.environment = environment;
	}
	public String getLighting() {
		return lighting;
	}
	public void setLighting(String lighting) {
		this.lighting = lighting;
	}
	public String getHair_color() {
		return hair_color;
	}
	public void setHair_color(String hair_color) {
		this.hair_color = hair_color;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getBlurry() {
		return blurry;
	}
	public void setBlurry(String blurry) {
		this.blurry = blurry;
	}
	public String getMustache() {
		return mustache;
	}
	public void setMustache(String mustache) {
		this.mustache = mustache;
	}
	public String getRace() {
		return race;
	}
	public void setRace(String race) {
		this.race = race;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getEye_wear() {
		return eye_wear;
	}
	public void setEye_wear(String eye_wear) {
		this.eye_wear = eye_wear;
	}
	public String getSmiling() {
		return smiling;
	}
	public void setSmiling(String smiling) {
		this.smiling = smiling;
	}
	public String getError() {
		return error;
	}
	public void setError(String error) {
		this.error = error;
	}
}
